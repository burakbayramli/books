# ENTER3
# ---------------------------------------------------------------------------

def enter3(s: State):
    """Generate molecules entering at stream boundaries and from the jet."""

    for n_side in range(6):
        if s.IB[n_side] != 1:
            continue
        # determine sweep dimensions
        if n_side < 2:
            ncs1, ncs2 = s.NCY, s.NCZ
        elif n_side < 4:
            ncs1, ncs2 = s.NCX, s.NCZ
        else:
            ncs1, ncs2 = s.NCX, s.NCY

        for nc1 in range(1, ncs1 + 1):
            for nc2 in range(1, ncs2 + 1):
                for l in range(MNSP):
                    vmp = math.sqrt(2.0 * s.BOLTZ * s.FTMP / s.SP[4, l])
                    if n_side < 2:
                        a = s.BME[n_side, l] * s.CH * s.CD / (s.FH * s.FD) + s.BMR[n_side, l]
                    elif n_side < 4:
                        a = s.BME[n_side, l] * s.CW * s.CD / (s.FW * s.FD) + s.BMR[n_side, l]
                    else:
                        a = s.BME[n_side, l] * s.CW * s.CH / (s.FW * s.FH) + s.BMR[n_side, l]
                    m_count = int(a)
                    s.BMR[n_side, l] = a - m_count

                    if m_count <= 0:
                        continue

                    # speed ratio for inward normal component
                    sc = 0.0
                    has_drift = False
                    if n_side < 2 and abs(s.VFX) > 1e-6:
                        sc = s.VFX / vmp if n_side == 0 else -s.VFX / vmp
                        has_drift = True
                    elif 1 < n_side < 4 and abs(s.VFY) > 1e-6:
                        sc = s.VFY / vmp if n_side == 2 else -s.VFY / vmp
                        has_drift = True
                    elif n_side > 3 and abs(s.VFZ) > 1e-6:
                        sc = s.VFZ / vmp if n_side == 4 else -s.VFZ / vmp
                        has_drift = True

                    fs1 = sc + math.sqrt(sc * sc + 2.0)
                    fs2 = 0.5 * (1.0 + sc * (2.0 * sc - fs1))

                    # determine cell mc (0-based)
                    if n_side == 0:
                        mc = (nc2 - 1) * s.NCX * s.NCY + (nc1 - 1) * s.NCX
                    elif n_side == 1:
                        mc = (nc2 - 1) * s.NCX * s.NCY + nc1 * s.NCX - 1
                    elif n_side == 2:
                        mc = (nc2 - 1) * s.NCX * s.NCY + (nc1 - 1)
                    elif n_side == 3:
                        mc = (nc2 - 1) * s.NCX * s.NCY + (s.NCY - 1) * s.NCX + (nc1 - 1)
                    elif n_side == 4:
                        mc = (nc2 - 1) * s.NCX + (nc1 - 1)
                    else:
                        mc = (s.NCZ - 1) * s.NCX * s.NCY + (nc2 - 1) * s.NCX + (nc1 - 1)
                    mc = max(0, min(MNC - 1, mc))

                    for _ in range(m_count):
                        if s.NM >= MNM:
                            print(" WARNING: EXCESS MOLECULE LIMIT - RESTART WITH INCREASED FNUM")
                            break
                        ni = s.NM
                        s.NM += 1

                        # inward normal component
                        if has_drift:
                            qa = 3.0
                            if sc < -3.0: qa = abs(sc) + 1.0
                            while True:
                                u  = -qa + 2.0 * qa * rf()
                                un = u + sc
                                if un < 0.0: continue
                                acc = (2.0 * un / fs1) * math.exp(fs2 - u * u)
                                if acc >= rf(): break
                            inward = un * vmp
                        else:
                            inward = math.sqrt(-math.log(rf())) * vmp

                        # two tangential components
                        tang1, tang2 = rvelc(vmp)

                        # assign velocity components
                        if n_side == 0:
                            s.PV[0, ni] =  inward
                            s.PV[1, ni] = tang1 + s.VFY
                            s.PV[2, ni] = tang2 + s.VFZ
                        elif n_side == 1:
                            s.PV[0, ni] = -inward
                            s.PV[1, ni] = tang1 + s.VFY
                            s.PV[2, ni] = tang2 + s.VFZ
                        elif n_side == 2:
                            s.PV[1, ni] =  inward
                            s.PV[0, ni] = tang1 + s.VFX
                            s.PV[2, ni] = tang2 + s.VFZ
                        elif n_side == 3:
                            s.PV[1, ni] = -inward
                            s.PV[0, ni] = tang1 + s.VFX
                            s.PV[2, ni] = tang2 + s.VFZ
                        elif n_side == 4:
                            s.PV[2, ni] =  inward
                            s.PV[0, ni] = tang1 + s.VFX
                            s.PV[1, ni] = tang2 + s.VFY
                        else:
                            s.PV[2, ni] = -inward
                            s.PV[0, ni] = tang1 + s.VFX
                            s.PV[1, ni] = tang2 + s.VFY

                        if s.ISPR[0, l] > 0:
                            s.PR[ni] = srot(s.FTMP, s.ISPR[0, l])

                        # position just inside boundary
                        if   n_side == 0: s.PP[0, ni] = s.CB[0] + 0.001 * s.CW
                        elif n_side == 1: s.PP[0, ni] = s.CB[1] - 0.001 * s.CW
                        elif n_side == 2: s.PP[1, ni] = s.CB[2] + 0.001 * s.CH
                        elif n_side == 3: s.PP[1, ni] = s.CB[3] - 0.001 * s.CH
                        elif n_side == 4: s.PP[2, ni] = s.CB[4] + 0.001 * s.CD
                        else:             s.PP[2, ni] = s.CB[5] - 0.001 * s.CD

                        # transverse positions
                        if n_side < 2:
                            s.PP[1, ni] = s.CG[2, mc] + rf() * s.CH
                            s.PP[2, ni] = s.CG[4, mc] + rf() * s.CD
                        elif n_side < 4:
                            s.PP[0, ni] = s.CG[0, mc] + rf() * s.CW
                            s.PP[2, ni] = s.CG[4, mc] + rf() * s.CD
                        else:
                            s.PP[0, ni] = s.CG[0, mc] + rf() * s.CW
                            s.PP[1, ni] = s.CG[2, mc] + rf() * s.CH

                        if MNSP > 1:
                            s.IPS[ni] = l

                        # sub-cell
                        mscx = int(((s.PP[0, ni] - s.CG[0, mc]) / s.CW) * (s.NSCX - 0.001)) + 1
                        mscy = int(((s.PP[1, ni] - s.CG[2, mc]) / s.CH) * (s.NSCY - 0.001)) + 1
                        mscz = int(((s.PP[2, ni] - s.CG[4, mc]) / s.CD) * (s.NSCZ - 0.001)) + 1
                        msc  = ((mscz - 1) * s.NSCX * s.NSCY +
                                (mscy - 1) * s.NSCX + (mscx - 1) +
                                s.NSCX * s.NSCY * s.NSCZ * mc)
                        s.IPL[ni] = max(0, min(MNSC - 1, msc))

    # --- jet ---
    if s.IJET > 0:
        for l in range(MNSP):
            vmp = math.sqrt(2.0 * s.BOLTZ * s.TMPJ / s.SP[4, l])
            a = s.BMEJ[l] + s.BMRJ[l]
            m_count = int(math.sqrt(a))
            s.BMRJ[l] = a - m_count * m_count

            if m_count <= 0:
                continue

            sc = s.FVJ / vmp if abs(s.FVJ) > 1e-6 else 0.0
            fs1 = sc + math.sqrt(sc * sc + 2.0)
            fs2 = 0.5 * (1.0 + sc * (2.0 * sc - fs1))

            for k_idx in range(1, m_count + 1):
                for kk_idx in range(1, m_count + 1):
                    if s.NM >= MNM:
                        print(" WARNING: EXCESS MOLECULE LIMIT - RESTART WITH INCREASED FNUM")
                        break
                    ni = s.NM
                    s.NM += 1

                    if abs(s.FVJ) > 1e-6:
                        qa = 3.0
                        if sc < -3.0: qa = abs(sc) + 1.0
                        while True:
                            u  = -qa + 2.0 * qa * rf()
                            un = u + sc
                            if un < 0.0: continue
                            acc = (2.0 * un / fs1) * math.exp(fs2 - u * u)
                            if acc >= rf(): break
                        s.PV[1, ni] = un * vmp
                    else:
                        s.PV[1, ni] = math.sqrt(-math.log(rf())) * vmp

                    tang1, tang2 = rvelc(vmp)
                    s.PV[0, ni] = tang1
                    s.PV[2, ni] = tang2

                    if s.ISPR[0, l] > 0:
                        s.PR[ni] = srot(s.TMPJ, s.ISPR[0, l])

                    s.PP[1, ni] = 0.001 * s.CH
                    s.IPS[ni]   = l

                    r_rad = math.sqrt((k_idx - rf()) / m_count) * s.RJ
                    azim  = 2.0 * s.PI * (kk_idx - rf()) / m_count
                    if s.IJET == 2: azim *= 0.5
                    if s.IJET == 3: azim *= 0.25

                    s.PP[0, ni] = s.XJ + r_rad * math.cos(azim)
                    s.PP[2, ni] = s.ZJ + r_rad * math.sin(azim)

                    mcx = int((s.PP[0, ni] - s.CB[0]) / s.CW + 0.99999)
                    mcx = max(0, min(s.NCX - 1, mcx))
                    mcz = int((s.PP[2, ni] - s.CB[4]) / s.CD + 0.99999)
                    mcz = max(0, min(s.NCZ - 1, mcz))
                    mc  = mcz * s.NCX * s.NCY + mcx

                    mscx = int(((s.PP[0, ni] - s.CG[0, mc]) / s.CW) * (s.NSCX - 0.001)) + 1
                    mscz = int(((s.PP[2, ni] - s.CG[4, mc]) / s.CD) * (s.NSCZ - 0.001)) + 1
                    msc  = ((mscz - 1) * s.NSCX * s.NSCY + (mscx - 1) +
                            s.NSCX * s.NSCY * s.NSCZ * mc)
                    s.IPL[ni] = max(0, min(MNSC - 1, msc))

# ---------------------------------------------------------------------------
# INDEXM
# ---------------------------------------------------------------------------

def indexm(s: State):
    """
    Build the cross-reference array IR so that molecules are ordered
    by group → cell → sub-cell.  Mirrors INDEXM.FOR exactly.
    """
    s.IG[1, :] = 0
    s.IC[1, :, :] = 0
    s.ISCG[1, :, :] = 0

    for n in range(s.NM):
        ls = s.IPS[n] if MNSP > 1 else 0
        mg = s.ISP[ls]       # 0-based group
        s.IG[1, mg] += 1
        msc = s.IPL[n]
        s.ISCG[1, msc, mg] += 1
        mc  = s.ISC[msc]
        s.IC[1, mc, mg] += 1

    # set start addresses
    m_addr = 0
    for l in range(MNSG):
        s.IG[0, l] = m_addr
        m_addr += s.IG[1, l]

    for l in range(MNSG):
        m_addr = s.IG[0, l]
        for n in range(MNC):
            s.IC[0, n, l] = m_addr
            m_addr += s.IC[1, n, l]

        m_addr = s.IG[0, l]
        for n in range(MNSC):
            s.ISCG[0, n, l] = m_addr
            m_addr += s.ISCG[1, n, l]
            s.ISCG[1, n, l] = 0

    # fill cross-reference array IR
    for n in range(s.NM):
        ls = s.IPS[n] if MNSP > 1 else 0
        mg = s.ISP[ls]
        msc = s.IPL[n]
        s.ISCG[1, msc, mg] += 1
        k = s.ISCG[0, msc, mg] + s.ISCG[1, msc, mg] - 1
        s.IR[k] = n

# ---------------------------------------------------------------------------
# SELECT  (collision pair selection)
# ---------------------------------------------------------------------------

def select_pair(s: State):
    """
    Select a collision pair and compute CVR = sigma_T * v_r.
    Results stored in s.L, s.M, s.LS, s.MS, s.VRC, s.VRR, s.VR, s.CVR.
    Uses 0-based indices throughout.
    """
    n  = s.N
    nn = s.NN
    mm = s.MM

    # first molecule from group NN in cell N
    cnt = s.IC[1, n, nn]
    k = int(rf() * (cnt - 0.001)) + s.IC[0, n, nn]
    s.L = s.IR[k]

    while True:
        msc = s.IPL[s.L]
        # find nearest sub-cell that has a group-MM molecule
        if (nn == mm and s.ISCG[1, msc, mm] == 1) or \
           (nn != mm and s.ISCG[1, msc, mm] == 0):
            nst, nsg = 1, 1
            found = False
            for _ in range(MNSC * 2):
                inc  = nsg * nst
                nsg  = -nsg
                nst += 1
                msc += inc
                if msc < 0 or msc >= MNSC: continue
                if s.ISC[msc] != n: continue
                if s.ISCG[1, msc, mm] >= 1:
                    found = True
                    break
            if not found:
                break  # no partner found; skip

        cnt2 = s.ISCG[1, msc, mm]
        k = int(rf() * (cnt2 - 0.001)) + s.ISCG[0, msc, mm]
        s.M = s.IR[k]
        if s.L == s.M:
            continue    # re-pick
        break

    s.VRC[:] = s.PV[:, s.L] - s.PV[:, s.M]
    s.VRR = float(np.dot(s.VRC, s.VRC))
    s.VR  = math.sqrt(s.VRR)

    ls = s.IPS[s.L] if MNSP > 1 else 0
    ms = s.IPS[s.M] if MNSP > 1 else 0
    s.LS = ls
    s.MS = ms

    # collision cross-section × relative speed  eqn (4.63)
    s.CVR = (s.VR * s.SPM[0, ls, ms] *
             ((2.0 * s.BOLTZ * s.SPM[1, ls, ms] /
               (s.SPM[4, ls, ms] * s.VRR)) **
              (s.SPM[2, ls, ms] - 0.5)) /
             s.SPM[5, ls, ms])

# ---------------------------------------------------------------------------
# ELASTIC
# ---------------------------------------------------------------------------

def elastic(s: State):
    """
    Generate post-collision velocities using VHS or VSS model.
    Mirrors ELASTIC.FOR.
    """
    ls, ms = s.LS, s.MS
    rml = s.SPM[4, ls, ms] / s.SP[4, ms]
    rmm = s.SPM[4, ls, ms] / s.SP[4, ls]

    vccm = rml * s.PV[:, s.L] + rmm * s.PV[:, s.M]

    vrcp = np.empty(3)
    if abs(s.SPM[3, ls, ms] - 1.0) < 1e-3:
        # VHS — isotropic scattering
        b = 2.0 * rf() - 1.0
        a = math.sqrt(1.0 - b * b)
        c = 2.0 * s.PI * rf()
        vrcp[0] = b * s.VR
        vrcp[1] = a * math.cos(c) * s.VR
        vrcp[2] = a * math.sin(c) * s.VR
    else:
        # VSS  eqn (2.22)
        b = 2.0 * (rf() ** s.SPM[3, ls, ms]) - 1.0
        a = math.sqrt(1.0 - b * b)
        c = 2.0 * s.PI * rf()
        oc, sc2 = math.cos(c), math.sin(c)
        d = math.sqrt(s.VRC[1] ** 2 + s.VRC[2] ** 2)
        if d > 1e-6:
            vrcp[0] = b * s.VRC[0] + a * sc2 * d
            vrcp[1] = b * s.VRC[1] + a * (s.VR * s.VRC[2] * oc - s.VRC[0] * s.VRC[1] * sc2) / d
            vrcp[2] = b * s.VRC[2] - a * (s.VR * s.VRC[1] * oc + s.VRC[0] * s.VRC[2] * sc2) / d
        else:
            vrcp[0] = b * s.VRC[0]
            vrcp[1] = a * oc * s.VRC[0]
            vrcp[2] = a * sc2 * s.VRC[0]

    s.PV[:, s.L] = vccm + vrcp * rmm
    s.PV[:, s.M] = vccm - vrcp * rml

# ---------------------------------------------------------------------------
# INELR  — rotational energy redistribution
# ---------------------------------------------------------------------------

def inelr(s: State):
    """
    Larsen-Borgnakke redistribution of rotational energy.
    Mirrors INELR.FOR.
    """
    ls, ms = s.LS, s.MS
    eti = 0.5 * s.SPM[4, ls, ms] * s.VRR
    eci = 0.0
    ecf = 0.0
    ecc = eti
    xib = 2.5 - s.SPM[2, ls, ms]
    irt = 0
    ir_flag = [0, 0]

    mols = [s.L, s.M]
    species = [ls, ms]
    partner = [ms, ls]

    for nsp in range(2):
        k_mol = mols[nsp]
        ks    = species[nsp]
        js    = partner[nsp]
        ir_flag[nsp] = 0
        if s.ISPR[0, ks] > 0:
            if s.ISPR[1, ks] == 0:
                atk = 1.0 / s.SPR[0, ks, js]
            else:
                atk = 1.0 / (s.SPR[0, ks, js] +
                              s.SPR[1, ks, js] * s.CT[s.N] +
                              s.SPR[2, ks, js] * s.CT[s.N] ** 2)
            if atk > rf():
                irt = 1
                ir_flag[nsp] = 1
                ecc += s.PR[k_mol]
                eci += s.PR[k_mol]
                xib += 0.5 * s.ISPR[0, ks]

    if irt == 1:
        for nsp in range(2):
            if ir_flag[nsp] == 1:
                k_mol = mols[nsp]
                ks    = species[nsp]
                xib  -= 0.5 * s.ISPR[0, ks]
                if s.ISPR[0, ks] == 2:
                    erm = 1.0 - rf() ** (1.0 / xib)
                else:
                    xia = 0.5 * s.ISPR[0, ks]
                    erm = lbs(xia - 1.0, xib - 1.0)
                s.PR[k_mol] = erm * ecc
                ecc -= s.PR[k_mol]
                ecf += s.PR[k_mol]

        etf = eti + eci - ecf
        a = math.sqrt(2.0 * etf / s.SPM[4, ls, ms])
        if abs(s.SPM[3, ls, ms] - 1.0) < 1e-3:
            s.VR = a
        else:
            s.VRC *= a / s.VR
            s.VR   = a

# ---------------------------------------------------------------------------
# COLLMR  — collision routine
# ---------------------------------------------------------------------------

def collmr(s: State):
    """
    Perform NTC collisions in all cells.  Mirrors COLLMR.FOR.
    """
    for n in range(MNC):
        s.N = n
        for nn in range(MNSG):
            s.NN = nn
            for mm in range(MNSG):
                s.MM = mm
                # average number of group-MM molecules
                sn = 0.0
                for k in range(MNSP):
                    if s.ISP[k] == mm:
                        sn += s.CS[0, n, k]
                if sn > 1.0:
                    avn = sn / float(s.NSMP) if s.NSMP > 0 else sn
                else:
                    avn = float(s.IC[1, n, mm])

                asel = (0.5 * s.IC[1, n, nn] * avn * s.FNUM *
                        s.CCG[0, n, nn, mm] * s.DTM / s.CC +
                        s.CCG[1, n, nn, mm])
                nsel = int(asel)
                s.CCG[1, n, nn, mm] = asel - nsel

                if nsel <= 0:
                    continue

                insufficient = ((nn != mm and
                                 (s.IC[1, n, nn] < 1 or s.IC[1, n, mm] < 1)) or
                                (nn == mm and s.IC[1, n, nn] < 2))
                if insufficient:
                    s.CCG[1, n, nn, mm] += nsel
                    continue

                cvm = s.CCG[0, n, nn, mm]
                s.SELT += nsel

                for _ in range(nsel):
                    select_pair(s)
                    if s.CVR > cvm:
                        cvm = s.CVR
                    if rf() < s.CVR / s.CCG[0, n, nn, mm]:
                        s.NCOL += 1.0
                        s.SEPT += math.sqrt(
                            (s.PP[0, s.L] - s.PP[0, s.M]) ** 2 +
                            (s.PP[1, s.L] - s.PP[1, s.M]) ** 2 +
                            (s.PP[2, s.L] - s.PP[2, s.M]) ** 2)
                        s.COL[s.LS, s.MS] += 1.0
                        s.COL[s.MS, s.LS] += 1.0

                        if s.ISPR[0, s.LS] > 0 or s.ISPR[0, s.MS] > 0:
                            inelr(s)
                        elastic(s)

                s.CCG[0, n, nn, mm] = cvm

# ---------------------------------------------------------------------------
# SAMPI3  — initialise sampling
# ---------------------------------------------------------------------------

def sampi3(s: State):
    """Reset all sampling accumulators.  Mirrors SAMPI3.FOR."""
    s.NSMP = 0
    s.TIMI = s.TIME
    for l in range(MNSP):
        for n in range(MNC):
            s.CS[0, n, l] = 1e-6
            s.CS[1:, n, l] = 0.0
            s.CSR[n, l] = 0.0
        for n in range(MNSE):
            s.CSS[0, n, l] = 1e-6
            s.ICS[n] = 0
            s.CSS[1:, n, l] = 0.0

# ---------------------------------------------------------------------------
# SAMPLE3  — accumulate flow-field samples
# ---------------------------------------------------------------------------

def sample3(s: State):
    """Sample molecules into CS accumulators.  Mirrors SAMPLE3.FOR."""
    s.NSMP += 1
    for nn in range(MNSG):
        for n in range(MNC):
            count = s.IC[1, n, nn]
            if count > 0:
                start = s.IC[0, n, nn]
                for j in range(count):
                    k = start + j
                    mol = s.IR[k]
                    i = s.IPS[mol] if MNSP > 1 else 0
                    s.CS[0, n, i] += 1.0
                    for ll in range(3):
                        s.CS[ll + 1, n, i] += s.PV[ll, mol]
                        s.CS[ll + 4, n, i] += s.PV[ll, mol] ** 2
                    if MNMR > 1:
                        s.CSR[n, i] += s.PR[mol]

# ---------------------------------------------------------------------------
# OUT3  — output results
# ---------------------------------------------------------------------------

def out3(s: State):
    """Write flow-field and surface results to DSMC3.OUT.  Mirrors OUT3.FOR."""
    with open('DSMC3.OUT', 'w') as f:
        f.write(f" FLOW SAMPLED FROM TIME {s.TIMI:.6e} TO TIME {s.TIME:.6e}\n")
        f.write(" COLLISIONS:-\n")
        for m in range(MNSP):
            row = "  ".join(f"{s.COL[m, l]:14.0f}" for l in range(MNSP))
            f.write(row + "\n")
        f.write(f" TOTAL NUMBER OF SAMPLES {s.NSMP}\n")
        f.write(f" {s.NM} MOLECULES\n")
        f.write(f" {s.MOVT:.0f} TOTAL MOLECULAR MOVES\n")
        if s.NCOL > 0:
            f.write(f" {int(s.SELT)} SELECTIONS  {int(s.NCOL)} COLLISION EVENTS"
                    f"  RATIO {s.NCOL / s.SELT:.6f}\n")
            f.write(f" MEAN COLLISION SEPARATION {s.SEPT / s.NCOL:.6e}\n")

        f.write("\n")
        for ks in range(2):
            if s.ISURF[ks] == 0:
                continue
            f.write(f" SURFACE {ks + 1}\n\n")
            nel = ((s.LIMS[ks, 2] - s.LIMS[ks, 1] + 1) *
                   (s.LIMS[ks, 4] - s.LIMS[ks, 3] + 1))
            if ks == 0:
                nel1, nel2 = 0, nel - 1
            else:
                off = ((s.LIMS[0, 2] - s.LIMS[0, 1] + 1) *
                       (s.LIMS[0, 4] - s.LIMS[0, 3] + 1))
                nel1 = off
                nel2 = off + nel - 1

            dt_inv = s.FNUM / (s.TIME - s.TIMI) if (s.TIME - s.TIMI) > 0 else 0.0

            f.write("    X COORD     Y COORD      Z COORD     SAMPLE"
                    "     FRACTION SPECIES 1, SPECIES 2....\n")
            for k in range(nel1, nel2 + 1):
                nc = s.ICS[k] - 1   # back to 0-based
                if nc < 0: continue
                isurf_val = s.ISURF[ks]
                if isurf_val < 3:
                    y_c = (s.LIMS[ks, 0] - 1) * s.CH + s.CB[2]
                    x_c = 0.5 * (s.CG[0, nc] + s.CG[1, nc])
                    z_c = 0.5 * (s.CG[4, nc] + s.CG[5, nc])
                elif isurf_val < 5:
                    x_c = (s.LIMS[ks, 0] - 1) * s.CW + s.CB[0]
                    y_c = 0.5 * (s.CG[2, nc] + s.CG[3, nc])
                    z_c = 0.5 * (s.CG[4, nc] + s.CG[5, nc])
                else:
                    z_c = (s.LIMS[ks, 0] - 1) * s.CD + s.CB[4]
                    y_c = 0.5 * (s.CG[2, nc] + s.CG[3, nc])
                    x_c = 0.5 * (s.CG[0, nc] + s.CG[1, nc])
                ss1 = sum(s.CSS[0, k, l] for l in range(MNSP))
                fracs = "  ".join(f"{s.CSS[0, k, l] / ss1:13.6f}" for l in range(MNSP))
                f.write(f"  {x_c:12.5f}  {y_c:12.5f}  {z_c:12.5f}  {ss1:12.1f}  {fracs}\n")

            f.write("     X COORD      Y COORD     Z COORD     NUM FLUX"
                    "    INC PRESS  REFL PRESS  INC SH1  REFL SH1"
                    "  INC SH2  REFL SH2  INC TR EN  REFL TR EN"
                    "  INC ROT EN  REFL ROT EN  NET HEAT FLUX\n")
            for k in range(nel1, nel2 + 1):
                nc = s.ICS[k] - 1
                if nc < 0: continue
                isurf_val = s.ISURF[ks]
                if isurf_val < 3:
                    y_c  = (s.LIMS[ks, 0] - 1) * s.CH + s.CB[2]
                    x_c  = 0.5 * (s.CG[0, nc] + s.CG[1, nc])
                    z_c  = 0.5 * (s.CG[4, nc] + s.CG[5, nc])
                    ar   = s.CW * s.CD
                elif isurf_val < 5:
                    x_c  = (s.LIMS[ks, 0] - 1) * s.CW + s.CB[0]
                    y_c  = 0.5 * (s.CG[2, nc] + s.CG[3, nc])
                    z_c  = 0.5 * (s.CG[4, nc] + s.CG[5, nc])
                    ar   = s.CH * s.CD
                else:
                    z_c  = (s.LIMS[ks, 0] - 1) * s.CD + s.CB[4]
                    y_c  = 0.5 * (s.CG[2, nc] + s.CG[3, nc])
                    x_c  = 0.5 * (s.CG[0, nc] + s.CG[1, nc])
                    ar   = s.CW * s.CH
                ss = np.sum(s.CSS[:, k, :], axis=1)   # sum over species
                ss *= dt_inv / ar
                f.write(f"  {x_c:13.5e}  {y_c:13.5e}  {z_c:13.5e}"
                        + "".join(f"  {ss[i]:13.5e}" for i in
                                  [0, 1, 2, 3, 8, 9, 10, 4, 5, 6, 7])
                        + f"  {ss[4]+ss[5]+ss[6]+ss[7]:13.5e}\n")

        # --- flow-field ---
        f.write(" FLOWFIELD PROPERTIES\n")
        f.write(" SAMPLES\n CELL     N SP 1    N SP 2     ETC\n")
        for n in range(MNC):
            vals = "  ".join(f"{s.CS[0, n, l]:12.0f}" for l in range(MNSP))
            f.write(f"  {n+1}  {vals}\n")

        f.write(" FLOWFIELD PROPERTIES\n")
        f.write("  CELL   X COORD   Y COORD   Z COORD   DENSITY"
                "   TR TEMP   ROT TEMP    OV TEMP      U         V         W\n")

        svel = np.zeros((3, MNC))
        fac  = s.FNUM / (s.CC * s.NSMP) if s.NSMP > 0 else 0.0

        for n in range(MNC):
            sn   = 0.0; sm = 0.0
            smu  = np.zeros(3)
            smcc = 0.0; sre = 0.0; srdf = 0.0

            for l in range(MNSP):
                sn   += s.CS[0, n, l]
                sm   += s.SP[4, l] * s.CS[0, n, l]
                for k in range(3):
                    smu[k] += s.SP[4, l] * s.CS[k+1, n, l]
                smcc += (s.CS[4, n, l] + s.CS[5, n, l] + s.CS[6, n, l]) * s.SP[4, l]
                sre  += s.CSR[n, l]
                srdf += s.ISPR[0, l] * s.CS[0, n, l]

            if sn < 1e-10:
                continue
            denn = sn * fac
            den  = denn * sm / sn
            vel  = smu / sm
            svel[:, n] = vel
            uu   = float(np.dot(vel, vel))
            tt   = (smcc - sm * uu) / (3.0 * s.BOLTZ * sn)
            trot = (2.0 / s.BOLTZ) * sre / srdf if srdf > 1e-6 else 0.0
            temp = (3.0 * tt + (srdf / sn) * trot) / (3.0 + srdf / sn) if srdf > 1e-6 else tt
            s.CT[n] = temp
            xc = 0.5 * (s.CG[0, n] + s.CG[1, n])
            yc = 0.5 * (s.CG[2, n] + s.CG[3, n])
            zc = 0.5 * (s.CG[4, n] + s.CG[5, n])
            f.write(f"  {n+1:5d}  {xc:9.5f}  {yc:9.5f}  {zc:9.5f}"
                    f"  {den:.4e}  {tt:9.4f}  {trot:9.4f}  {temp:9.4f}"
                    f"  {vel[0]:9.4f}  {vel[1]:9.4f}  {vel[2]:9.4f}\n")

        for l in range(MNSP):
            f.write(f" SPECIES {l+1}\n")
            f.write("  CELL   X COORD    Y COORD    Z COORD   N DENS"
                    "    DENSITY    TTX    TTY    TTZ   TR TEMP"
                    "   ROT TEMP    TEMP    U DIF   V DIF   W DIF\n")
            for n in range(MNC):
                if s.CS[0, n, l] < 1e-10:
                    continue
                denn = s.CS[0, n, l] * fac
                den  = s.SP[4, l] * denn
                vel  = s.CS[1:4, n, l] / s.CS[0, n, l]
                uu   = float(np.dot(vel, vel))
                ttx  = (s.SP[4, l] / s.BOLTZ) * (s.CS[4, n, l] / s.CS[0, n, l] - vel[0] ** 2)
                tty  = (s.SP[4, l] / s.BOLTZ) * (s.CS[5, n, l] / s.CS[0, n, l] - vel[1] ** 2)
                ttz  = (s.SP[4, l] / s.BOLTZ) * (s.CS[6, n, l] / s.CS[0, n, l] - vel[2] ** 2)
                tt   = (s.SP[4, l] / (3.0 * s.BOLTZ)) * (
                        (s.CS[4, n, l] + s.CS[5, n, l] + s.CS[6, n, l]) / s.CS[0, n, l] - uu)
                if s.ISPR[0, l] > 0:
                    trot = 2.0 * s.CSR[n, l] / (s.ISPR[0, l] * s.BOLTZ * s.CS[0, n, l])
                else:
                    trot = 0.0
                temp = (3.0 * tt + s.ISPR[0, l] * trot) / (3.0 + s.ISPR[0, l])
                diff_vel = vel - svel[:, n]
                xc = 0.5 * (s.CG[0, n] + s.CG[1, n])
                yc = 0.5 * (s.CG[2, n] + s.CG[3, n])
                zc = 0.5 * (s.CG[4, n] + s.CG[5, n])
                f.write(f"  {n+1:5d}  {xc:9.5f}  {yc:9.5f}  {zc:9.5f}"
                        f"  {denn:.4e}  {den:.4e}"
                        f"  {ttx:9.4f}  {tty:9.4f}  {ttz:9.4f}"
                        f"  {tt:9.4f}  {trot:9.4f}  {temp:9.4f}"
                        f"  {diff_vel[0]:9.4f}  {diff_vel[1]:9.4f}  {diff_vel[2]:9.4f}\n")

