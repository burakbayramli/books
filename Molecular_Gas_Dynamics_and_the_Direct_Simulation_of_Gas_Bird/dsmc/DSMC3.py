def remove(s: State, n: int):
    """
    Remove molecule n (0-based) by replacing it with the last molecule NM-1.
    Decrements NM and returns the new n (n-1, because the caller loop
    should re-examine the slot, now occupied by the formerly-last molecule).
    """
    last = s.NM - 1
    s.PP[:, n]  = s.PP[:, last]
    s.PV[:, n]  = s.PV[:, last]
    s.IPL[n]    = s.IPL[last]
    if MNMR > 1:
        s.PR[n] = s.PR[last]
    if MNSP > 1:
        s.IPS[n] = s.IPS[last]
    s.NM -= 1
    return n - 1

