def main():
    nql  = int(input(" INPUT 0,1 FOR CONTINUING,NEW CALCULATION: "))
    nqls = int(input(" INPUT 0,1 FOR CONTINUING,NEW SAMPLE: "))

    if nql == 1:
        s = State()
        init3(s)
    else:
        s = read_restart()

    if nqls == 1:
        sampi3(s)

    while True:
        s.NPR += 1

        if s.NPR <= s.NPS:
            sampi3(s)

        for jjj in range(s.NSP):
            for iii in range(s.NIS):
                s.TIME += s.DTM
                print(f" DSMC3:- Move {iii+1:5d}{jjj+1:5d} of {s.NIS:5d}{s.NSP:5d}"
                      f"  {s.NM:8d} Mols  {s.NCOL:14.0f} Colls")
                move3(s)
                indexm(s)
                collmr(s)

            sample3(s)

        print(f" WRITING RESTART AND OUTPUT FILES  {s.NPR}  OF  {s.NPT}")
        write_restart(s)
        out3(s)

        if s.NPR >= s.NPT:
            break

    print(" DONE.")

