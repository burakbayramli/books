def reflect3(s: State, n: int, ks: int, k: int, xc: float, yc: float, zc: float, mc: int):
    """
    Reflect molecule n (0-based) off surface ks (0-based) at element k (0-based),
    position (xc,yc,zc), adjacent cell mc (0-based).
    Samples incident/reflected surface properties and updates velocity.
    """
    if s.ICS[k] == 0:
        s.ICS[k] = mc + 1    # store 1-based cell reference (0 = unvisited sentinel)

    l = s.IPS[n] if MNSP > 1 else 0

    # --- sample incident ---
    s.CSS[0, k, l] += 1.0
    isurf_val = s.ISURF[ks]
    m5l = s.SP[4, l]   # molecular mass
    vx, vy, vz = s.PV[0, n], s.PV[1, n], s.PV[2, n]

    if   isurf_val == 1:
        s.CSS[1, k, l] -= m5l * vy
        s.CSS[3, k, l] += m5l * vx
        s.CSS[9, k, l] += m5l * vz
    elif isurf_val == 2:
        s.CSS[1, k, l] += m5l * vy
        s.CSS[3, k, l] += m5l * vx
        s.CSS[9, k, l] += m5l * vz
    elif isurf_val == 3:
        s.CSS[1, k, l] -= m5l * vx
        s.CSS[3, k, l] += m5l * vy
        s.CSS[9, k, l] += m5l * vz
    elif isurf_val == 4:
        s.CSS[1, k, l] += m5l * vx
        s.CSS[3, k, l] += m5l * vy
        s.CSS[9, k, l] += m5l * vz
    elif isurf_val == 5:
        s.CSS[1, k, l] -= m5l * vz
        s.CSS[3, k, l] += m5l * vx
        s.CSS[9, k, l] += m5l * vy
    elif isurf_val == 6:
        s.CSS[1, k, l] += m5l * vz
        s.CSS[3, k, l] += m5l * vx
        s.CSS[9, k, l] += m5l * vy

    s.CSS[4, k, l] += 0.5 * m5l * (vx**2 + vy**2 + vz**2)
    if MNMR > 1:
        s.CSS[6, k, l] += s.PR[n]

    # --- reflection ---
    if s.TSURF[ks] < 0.0:
        # specular
        if isurf_val in (1, 2): s.PV[1, n] = -s.PV[1, n]
        if isurf_val in (3, 4): s.PV[0, n] = -s.PV[0, n]
        if isurf_val in (5, 6): s.PV[2, n] = -s.PV[2, n]
    else:
        # diffuse
        vmp = math.sqrt(2.0 * s.BOLTZ * s.TSURF[ks] / m5l)
        if isurf_val == 1:
            s.PV[1, n] = math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[0, n] = u;  s.PV[2, n] = v
        elif isurf_val == 2:
            s.PV[1, n] = -math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[0, n] = u;  s.PV[2, n] = v
        elif isurf_val == 3:
            s.PV[0, n] = math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[1, n] = u;  s.PV[2, n] = v
        elif isurf_val == 4:
            s.PV[0, n] = -math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[1, n] = u;  s.PV[2, n] = v
        elif isurf_val == 5:
            s.PV[2, n] = math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[1, n] = u;  s.PV[0, n] = v
        elif isurf_val == 6:
            s.PV[2, n] = -math.sqrt(-math.log(rf())) * vmp
            u, v = rvelc(vmp);  s.PV[1, n] = u;  s.PV[0, n] = v
        if s.ISPR[0, l] > 0:
            s.PR[n] = srot(s.TSURF[ks], s.ISPR[0, l])

    # --- reposition just off surface ---
    if   isurf_val == 1:
        s.PP[0, n] = xc;  s.PP[1, n] = yc + 0.001 * s.CH;  s.PP[2, n] = zc
    elif isurf_val == 2:
        s.PP[0, n] = xc;  s.PP[1, n] = yc - 0.001 * s.CH;  s.PP[2, n] = zc
    elif isurf_val == 3:
        s.PP[0, n] = xc + 0.001 * s.CW;  s.PP[1, n] = yc;  s.PP[2, n] = zc
    elif isurf_val == 4:
        s.PP[0, n] = xc - 0.001 * s.CW;  s.PP[1, n] = yc;  s.PP[2, n] = zc
    elif isurf_val == 5:
        s.PP[0, n] = xc;  s.PP[1, n] = yc;  s.PP[2, n] = zc + 0.001 * s.CD
    elif isurf_val == 6:
        s.PP[0, n] = xc;  s.PP[1, n] = yc;  s.PP[2, n] = zc - 0.001 * s.CD

    s.IPL[n] = mc * s.NSCX * s.NSCY * s.NSCZ   # first sub-cell of cell mc

    # --- sample reflected ---
    vx2, vy2, vz2 = s.PV[0, n], s.PV[1, n], s.PV[2, n]
    if   isurf_val == 1: s.CSS[2, k, l] += m5l * vy2
    elif isurf_val == 2: s.CSS[2, k, l] -= m5l * vy2
    elif isurf_val == 3: s.CSS[2, k, l] += m5l * vx2
    elif isurf_val == 4: s.CSS[2, k, l] -= m5l * vx2
    elif isurf_val == 5: s.CSS[2, k, l] += m5l * vz2
    elif isurf_val == 6: s.CSS[2, k, l] -= m5l * vz2

    if isurf_val < 3:
        s.CSS[8, k, l]  -= m5l * vx2
        s.CSS[10, k, l] -= m5l * vz2
    elif isurf_val < 5:
        s.CSS[8, k, l]  -= m5l * vy2
        s.CSS[10, k, l] -= m5l * vz2
    else:
        s.CSS[8, k, l]  -= m5l * vx2
        s.CSS[10, k, l] -= m5l * vy2

    s.CSS[5, k, l] -= 0.5 * m5l * (vx2**2 + vy2**2 + vz2**2)
    if MNMR > 1:
        s.CSS[7, k, l] -= s.PR[n]

