def init3(s: State):
    """Initialise simulation: set constants, call data3, build geometry,
    place initial molecules, compute boundary fluxes."""

    # --- defaults ---
    s.FND  = 0.0
    s.FTMP = 273.0
    s.VFX = s.VFY = s.VFZ = 0.0
    for n in range(6):
        s.IB[n] = 3
        for l in range(MNSP):
            s.ISP[l] = 0      # 0-based group
            s.FSP[l] = 0.0
            s.BME[n, l] = 0.0
            s.BMR[n, l] = 0.0
    s.IJET = 0
    for l in range(MNSP):
        s.BMEJ[l] = 0.0
        s.BMRJ[l] = 0.0

    data3(s)

    # --- cross-collision / mixture data ---
    if MNSP == 1:
        s.ISPD = 0

    for n in range(MNSP):
        for m in range(MNSP):
            if s.ISPR[2, n] == 0 and m != n:
                s.SPR[0, n, m] = s.SPR[0, n, n]
                s.SPR[1, n, m] = s.SPR[1, n, n]
                s.SPR[2, n, m] = s.SPR[2, n, n]
            if s.ISPD == 0 or n == m:
                s.SPM[0, n, m] = 0.25 * s.PI * (s.SP[0, n] + s.SP[0, m]) ** 2
                s.SPM[1, n, m] = 0.5 * (s.SP[1, n] + s.SP[1, m])
                s.SPM[2, n, m] = 0.5 * (s.SP[2, n] + s.SP[2, m])
                s.SPM[3, n, m] = 0.5 * (s.SP[3, n] + s.SP[3, m])
            else:
                s.SPM[0, n, m] = s.PI * s.SPM[0, n, m] ** 2
            # reduced mass  eqn (2.7)
            s.SPM[4, n, m] = (s.SP[4, n] / (s.SP[4, n] + s.SP[4, m])) * s.SP[4, m]
            s.SPM[5, n, m] = gamma_func(2.5 - s.SPM[2, n, m])

    # --- initialise counters ---
    s.TIME = 0.0
    s.NM   = 0
    s.NPR  = 0
    s.NCOL = 0.0
    s.MOVT = 0.0
    s.SELT = 0.0
    s.SEPT = 0.0
    s.COL[:, :] = 0.0

    # --- geometry ---
    s.FW = s.CB[1] - s.CB[0]
    s.FH = s.CB[3] - s.CB[2]
    s.FD = s.CB[5] - s.CB[4]
    s.CW = s.FW / s.NCX
    s.CH = s.FH / s.NCY
    s.CD = s.FD / s.NCZ
    s.CC = s.CW * s.CH * s.CD

    # set cell coordinates  (0-based cell index m)
    for mz in range(s.NCZ):
        for my in range(s.NCY):
            for mx in range(s.NCX):
                m = mz * s.NCX * s.NCY + my * s.NCX + mx   # 0-based
                s.CT[m] = s.FTMP
                # x
                s.CG[0, m] = s.CB[0] + mx * s.CW
                s.CG[1, m] = s.CG[0, m] + s.CW
                # y
                s.CG[2, m] = s.CB[2] + my * s.CH
                s.CG[3, m] = s.CG[2, m] + s.CH
                # z
                s.CG[4, m] = s.CB[4] + mz * s.CD
                s.CG[5, m] = s.CG[4, m] + s.CD
                # initialise CCG
                for lg in range(MNSG):
                    for kg in range(MNSG):
                        s.CCG[1, m, lg, kg] = rf()
                        s.CCG[0, m, lg, kg] = (s.SPM[0, 0, 0] * 300.0 *
                                                math.sqrt(s.FTMP / 300.0))

    # set sub-cells  (each sub-cell maps to its parent cell, 0-based)
    for n in range(MNC):
        for j in range(s.NSCZ):
            for mv in range(s.NSCY):
                for k in range(s.NSCX):
                    ll = (n * s.NSCX * s.NSCY * s.NSCZ +
                          j * s.NSCX * s.NSCY +
                          mv * s.NSCX + k)
                    if ll < MNSC:
                        s.ISC[ll] = n

    # --- place initial molecules ---
    if s.IIS > 0 and s.ISG > 0:
        for l in range(MNSP):
            rem = 0.0
            vmp = math.sqrt(2.0 * s.BOLTZ * s.FTMP / s.SP[4, l])
            for n in range(MNC):
                a = s.FND * s.CC * s.FSP[l] / s.FNUM + rem
                if n < MNC - 1:
                    mm = int(a)
                    rem = a - mm
                else:
                    mm = round(a)
                for _ in range(mm):
                    if s.NM < MNM:
                        ni = s.NM          # 0-based molecule index
                        s.NM += 1
                        if MNSP > 1:
                            s.IPS[ni] = l
                        s.PP[0, ni] = s.CG[0, n] + rf() * (s.CG[1, n] - s.CG[0, n])
                        s.PP[1, ni] = s.CG[2, n] + rf() * (s.CG[3, n] - s.CG[2, n])
                        s.PP[2, ni] = s.CG[4, n] + rf() * (s.CG[5, n] - s.CG[4, n])
                        ncolm = int((s.PP[0, ni] - s.CG[0, n]) * (s.NSCX - 0.001) / s.CW) + 1
                        nrow  = int((s.PP[1, ni] - s.CG[2, n]) * (s.NSCY - 0.001) / s.CH) + 1
                        nslc  = int((s.PP[2, ni] - s.CG[4, n]) * (s.NSCZ - 0.001) / s.CD) + 1
                        ipl = (n * s.NSCX * s.NSCY * s.NSCZ +
                               (nslc - 1) * s.NSCX * s.NSCY +
                               (nrow - 1) * s.NSCX + (ncolm - 1))
                        s.IPL[ni] = max(0, min(MNSC - 1, ipl))
                        for k in range(3):
                            u, v = rvelc(vmp)
                            s.PV[k, ni] = u       # second component discarded per call
                        # re-do properly: each component independently
                        u, _ = rvelc(vmp);  s.PV[0, ni] = u + s.VFX
                        u, _ = rvelc(vmp);  s.PV[1, ni] = u + s.VFY
                        u, _ = rvelc(vmp);  s.PV[2, ni] = u + s.VFZ
                        if s.ISPR[0, l] > 0:
                            s.PR[ni] = srot(s.FTMP, s.ISPR[0, l])
        print(f" {s.NM} MOLECULES")

    # --- boundary entry fluxes ---
    if s.IIS > 0:
        for n in range(6):
            if s.IB[n] == 1:
                print(f" side {n+1}")
                for l in range(MNSP):
                    vmp = math.sqrt(2.0 * s.BOLTZ * s.FTMP / s.SP[4, l])
                    if   n == 0: sc =  s.VFX / vmp
                    elif n == 1: sc = -s.VFX / vmp
                    elif n == 2: sc =  s.VFY / vmp
                    elif n == 3: sc = -s.VFY / vmp
                    elif n == 4: sc =  s.VFZ / vmp
                    else:        sc = -s.VFZ / vmp
                    if abs(sc) < 10.1:
                        a = (math.exp(-sc*sc) + s.SPI * sc * (1.0 + erf_func(sc))) / (2.0 * s.SPI)
                    elif sc > 10.0:
                        a = sc
                    else:
                        a = 0.0
                    if n < 2:
                        s.BME[n, l] = s.FND * s.FSP[l] * a * vmp * s.DTM * s.FH * s.FD / s.FNUM
                    elif n < 4:
                        s.BME[n, l] = s.FND * s.FSP[l] * a * vmp * s.DTM * s.FW * s.FD / s.FNUM
                    else:
                        s.BME[n, l] = s.FND * s.FSP[l] * a * vmp * s.DTM * s.FH * s.FW / s.FNUM
                    print(f"   species {l+1} entering mols {s.BME[n, l]:.4f}")

    # --- jet fluxes ---
    if s.IJET > 0:
        for l in range(MNSP):
            vmp = math.sqrt(2.0 * s.BOLTZ * s.TMPJ / s.SP[4, l])
            sc  = s.FVJ / vmp
            if abs(sc) < 10.1:
                a = (math.exp(-sc*sc) + s.SPI * sc * (1.0 + erf_func(sc))) / (2.0 * s.SPI)
            elif sc > 10.0:
                a = sc
            else:
                a = 0.0
            aj = s.PI * s.RJ * s.RJ
            if s.IJET == 2: aj *= 0.5
            if s.IJET == 3: aj *= 0.25
            s.BMEJ[l] = s.FNDJ * s.FSPJ[l] * a * vmp * s.DTM * aj / s.FNUM
            print(f"   entering mols in jet {s.BMEJ[l]:.4f}")

