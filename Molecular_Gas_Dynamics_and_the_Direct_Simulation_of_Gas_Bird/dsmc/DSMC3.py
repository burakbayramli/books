def srot(temp, idf):
    """
    Sample equilibrium rotational energy at temperature temp
    with idf rotational degrees of freedom.
    Replaces SROT(PR, TEMP, IDF).  Returns PR.
    """
    boltz = 1.380622e-23
    if idf == 2:
        pr = -math.log(rf()) * boltz * temp
    else:
        a = 0.5 * idf - 1.0
        while True:
            erm = rf() * 10.0
            b = ((erm / a) ** a) * math.exp(a - erm)
            if b >= rf():
                break
        pr = erm * boltz * temp
    return pr

def lbs(xma, xmb):
    """
    Select a Larsen-Borgnakke energy ratio using eqn (11.9).
    Replaces LBS(XMA, XMB, ERM).  Returns erm.
    """
    while True:
        erm = rf()
        if xma < 1e-6 or xmb < 1e-6:
            if xma < 1e-6 and xmb < 1e-6:
                return erm
            if xma < 1e-6:
                p = (1.0 - erm) ** xmb
            else:
                p = (1.0 - erm) ** xma
        else:
            p = (((xma + xmb) * erm / xma) ** xma) * \
                (((xma + xmb) * (1.0 - erm) / xmb) ** xmb)
        if p >= rf():
            return erm

