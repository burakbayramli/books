def move3(s: State):
    """
    Move all NM molecules over DTM.  Handle surface reflections, boundary
    conditions, and entry of new molecules.
    Mirrors MOVE3.FOR with GOTO replaced by while/continue.
    """
    ift = -1   # negative → molecules have not yet entered this step
    n = 0
    while n < s.NM:
        if ift < 0:
            at = s.DTM
        else:
            at = rf() * s.DTM

        # inner loop  (replaces GO TO 150)
        while True:
            s.MOVT += 1
            msc = s.IPL[n]
            mc  = s.ISC[msc]

            # bounds check — remove strayed molecules
            xi = s.PP[0, n]
            yi = s.PP[1, n]
            zi = s.PP[2, n]
            if (xi + 0.00001 * s.CW < s.CB[0] or xi - 0.00001 * s.CW > s.CB[1] or
                yi + 0.00001 * s.CH < s.CB[2] or yi - 0.00001 * s.CH > s.CB[3] or
                zi + 0.00001 * s.CD < s.CB[4] or zi - 0.00001 * s.CD > s.CB[5]):
                n = remove(s, n)
                break    # go to next n

            dx = s.PV[0, n] * at
            dy = s.PV[1, n] * at
            dz = s.PV[2, n] * at
            x  = xi + dx
            y  = yi + dy
            z  = zi + dz

            # --- surface intersection check ---
            hit_surface = False
            for ks in range(2):
                if s.ISURF[ks] == 0:
                    continue
                isurf_val = s.ISURF[ks]

                if isurf_val in (1, 2):
                    l1 = s.LIMS[ks, 0]
                    ys = (l1 - 1) * s.CH + s.CB[2]
                    cond = ((isurf_val == 1 and yi > ys and y < ys) or
                            (isurf_val == 2 and yi < ys and y > ys))
                    if cond and abs(dy) > 0.0:
                        xc = xi + (ys - yi) * dx / dy
                        zc = zi + (ys - yi) * dz / dy
                        if (s.CB[0] < xc < s.CB[1] and s.CB[4] < zc < s.CB[5]):
                            l2, l3 = s.LIMS[ks, 1], s.LIMS[ks, 2]
                            l4, l5 = s.LIMS[ks, 3], s.LIMS[ks, 4]
                            xsu = (l2 - 1) * s.CW + s.CB[0]
                            xsd = l3 * s.CW + s.CB[0]
                            zsu = (l4 - 1) * s.CD + s.CB[4]
                            zsd = l5 * s.CD + s.CB[4]
                            if xsu < xc < xsd and zsu < zc < zsd:
                                mcx = int((xc - s.CB[0]) / s.CW + 0.99999)
                                mcx = max(0, min(s.NCX - 1, mcx))
                                mcz = int((zc - s.CB[4]) / s.CD + 0.99999)
                                mcz = max(0, min(s.NCZ - 1, mcz))
                                mcsx = mcx - (l2 - 1)
                                mcsz = mcz - (l4 - 1)
                                mcs  = mcsz * (l3 - l2 + 1) + mcsx  # 0-based
                                if ks == 1:
                                    mcs += (s.LIMS[0, 2] - s.LIMS[0, 1] + 1) * \
                                           (s.LIMS[0, 4] - s.LIMS[0, 3] + 1)
                                mc2 = mcz * s.NCX * s.NCY + mcx
                                if isurf_val == 1:
                                    mc2 += (l1 - 1) * s.NCX
                                else:
                                    mc2 += (l1 - 2) * s.NCX
                                at = at * (y - ys) / dy
                                reflect3(s, n, ks, mcs, xc, ys, zc, mc2)
                                hit_surface = True
                                break

                elif isurf_val in (3, 4):
                    l1 = s.LIMS[ks, 0]
                    xs_val = (l1 - 1) * s.CW + s.CB[0]
                    cond = ((isurf_val == 3 and xi > xs_val and x < xs_val) or
                            (isurf_val == 4 and xi < xs_val and x > xs_val))
                    if cond and abs(dx) > 0.0:
                        yc = yi + (xs_val - xi) * dy / dx
                        zc = zi + (xs_val - xi) * dz / dx
                        if s.CB[2] < yc < s.CB[3] and s.CB[4] < zc < s.CB[5]:
                            l2, l3 = s.LIMS[ks, 1], s.LIMS[ks, 2]
                            l4, l5 = s.LIMS[ks, 3], s.LIMS[ks, 4]
                            ysu = (l2 - 1) * s.CH + s.CB[2]
                            ysd = l3 * s.CH + s.CB[2]
                            zsu = (l4 - 1) * s.CD + s.CB[4]
                            zsd = l5 * s.CD + s.CB[4]
                            if ysu < yc < ysd and zsu < zc < zsd:
                                mcy = int((yc - s.CB[2]) / s.CH + 0.99999)
                                mcy = max(0, min(s.NCY - 1, mcy))
                                mcz = int((zc - s.CB[4]) / s.CD + 0.99999)
                                mcz = max(0, min(s.NCZ - 1, mcz))
                                mcsy = mcy - (l2 - 1)
                                mcsz = mcz - (l4 - 1)
                                mcs  = mcsz * (l3 - l2 + 1) + mcsy
                                if ks == 1:
                                    mcs += (s.LIMS[0, 2] - s.LIMS[0, 1] + 1) * \
                                           (s.LIMS[0, 4] - s.LIMS[0, 3] + 1)
                                mc2 = mcz * s.NCX * s.NCY + (mcy) * s.NCX
                                if isurf_val == 3:
                                    mc2 += l1       # l1 is 1-based → becomes 0-based +1 offset
                                else:
                                    mc2 += l1 - 1
                                at = at * (x - xs_val) / dx
                                reflect3(s, n, ks, mcs, xs_val, yc, zc, mc2)
                                hit_surface = True
                                break

                elif isurf_val in (5, 6):
                    l1 = s.LIMS[ks, 0]
                    if l1 <= s.NCZ:
                        zs = (l1 - 1) * s.CD + s.CB[4]
                    else:
                        zs = s.CB[5]
                        l1 -= 1
                    cond = ((isurf_val == 5 and zi > zs and z < zs) or
                            (isurf_val == 6 and zi < zs and z > zs))
                    if cond and abs(dz) > 0.0:
                        yc = yi + (zs - zi) * dy / dz
                        xc = xi + (zs - zi) * dx / dz
                        if s.CB[0] < xc < s.CB[1] and s.CB[2] < yc < s.CB[3]:
                            l2, l3 = s.LIMS[ks, 1], s.LIMS[ks, 2]
                            l4, l5 = s.LIMS[ks, 3], s.LIMS[ks, 4]
                            xsu = (l2 - 1) * s.CW + s.CB[0]
                            xsd = l3 * s.CW + s.CB[0]
                            ysu = (l4 - 1) * s.CH + s.CB[2]
                            ysd = l5 * s.CH + s.CB[2]
                            if xsu < xc < xsd and ysu < yc < ysd:
                                mcy = int((yc - s.CB[2]) / s.CH + 0.99999)
                                mcy = max(0, min(s.NCY - 1, mcy))
                                mcx = int((xc - s.CB[0]) / s.CW + 0.99999)
                                mcx = max(0, min(s.NCX - 1, mcx))
                                mcsx = mcx - (l2 - 1)
                                mcsy = mcy - (l4 - 1)
                                mcs  = mcsy * (l3 - l2 + 1) + mcsx
                                if ks == 1:
                                    mcs += (s.LIMS[0, 2] - s.LIMS[0, 1] + 1) * \
                                           (s.LIMS[0, 4] - s.LIMS[0, 3] + 1)
                                mc2 = mcy * s.NCX + mcx
                                if isurf_val == 5:
                                    mc2 += (l1 - 1) * s.NCX * s.NCY
                                else:
                                    mc2 += (l1 - 2) * s.NCX * s.NCY
                                at = at * (z - zs) / dz
                                reflect3(s, n, ks, mcs, xc, yc, zs, mc2)
                                hit_surface = True
                                break

            if hit_surface:
                continue    # redo inner loop (molecule still moving)

            # --- outer boundary handling (Rob Nance's multi-boundary logic) ---
            ibound = 0
            icount = 0
            if x < s.CB[0] or x > s.CB[1]:
                ibound = 1;  icount += 1
            if y < s.CB[2] or y > s.CB[3]:
                ibound = 2;  icount += 1
            if z < s.CB[4] or z > s.CB[5]:
                ibound = 3;  icount += 1

            if icount > 1:
                dt3 = [5.0 * s.DTM] * 3
                if abs(s.PV[0, n]) > 1e-6:
                    dt1 = (s.CB[0] - s.PP[0, n]) / s.PV[0, n]
                    dt2 = (s.CB[1] - s.PP[0, n]) / s.PV[0, n]
                    dt3[0] = max(dt1, dt2)
                if abs(s.PV[1, n]) > 1e-6:
                    dt1 = (s.CB[2] - s.PP[1, n]) / s.PV[1, n]
                    dt2 = (s.CB[3] - s.PP[1, n]) / s.PV[1, n]
                    dt3[1] = max(dt1, dt2)
                if abs(s.PV[2, n]) > 1e-6:
                    dt1 = (s.CB[4] - s.PP[2, n]) / s.PV[2, n]
                    dt2 = (s.CB[5] - s.PP[2, n]) / s.PV[2, n]
                    dt3[2] = max(dt1, dt2)
                dtmin = 5.0 * s.DTM
                for kk in range(3):
                    if dt3[kk] < dtmin:
                        dtmin = dt3[kk]
                        ibound = kk + 1

            if ibound == 1:
                k_side = 0 if x < s.CB[0] else 1
                if s.IB[k_side] == 2:   # symmetry
                    at = at * (x - s.CB[k_side]) / dx
                    s.PP[0, n] = x - at * s.PV[0, n] + 0.001 * s.CW * (3.0 - 2.0 * (k_side + 1))
                    s.PP[1, n] = y - at * s.PV[1, n]
                    s.PP[2, n] = z - at * s.PV[2, n]
                    s.PV[0, n] = -s.PV[0, n]
                    continue
                else:
                    n = remove(s, n)
                    break

            if ibound == 2:
                k_side = 2 if y < s.CB[2] else 3
                if s.IB[k_side] == 2:
                    at = at * (y - s.CB[k_side]) / dy
                    s.PP[0, n] = x - at * s.PV[0, n]
                    s.PP[1, n] = y - at * s.PV[1, n] + 0.001 * s.CH * (7.0 - 2.0 * (k_side + 1))
                    s.PP[2, n] = z - at * s.PV[2, n]
                    s.PV[1, n] = -s.PV[1, n]
                    continue
                else:
                    n = remove(s, n)
                    break

            if ibound == 3:
                k_side = 4 if z < s.CB[4] else 5
                if s.IB[k_side] == 2:
                    at = at * (z - s.CB[k_side]) / dz
                    s.PP[0, n] = x - at * s.PV[0, n]
                    s.PP[1, n] = y - at * s.PV[1, n]
                    s.PP[2, n] = z - at * s.PV[2, n] + 0.001 * s.CD * (11.0 - 2.0 * (k_side + 1))
                    s.PV[2, n] = -s.PV[2, n]
                    continue
                else:
                    n = remove(s, n)
                    break

            # --- update cell assignment ---
            if (x < s.CG[0, mc] or x > s.CG[1, mc] or
                y < s.CG[2, mc] or y > s.CG[3, mc] or
                z < s.CG[4, mc] or z > s.CG[5, mc]):
                mcx = int((x - s.CB[0]) / s.CW + 0.99999)
                mcx = max(0, min(s.NCX - 1, mcx))
                mcy = int((y - s.CB[2]) / s.CH + 0.99999)
                mcy = max(0, min(s.NCY - 1, mcy))
                mcz = int((z - s.CB[4]) / s.CD + 0.99999)
                mcz = max(0, min(s.NCZ - 1, mcz))
                mc  = mcz * s.NCX * s.NCY + mcy * s.NCX + mcx

            mscx = int(((x - s.CG[0, mc]) / s.CW) * (s.NSCX - 0.001)) + 1
            mscy = int(((y - s.CG[2, mc]) / s.CH) * (s.NSCY - 0.001)) + 1
            mscz = int(((z - s.CG[4, mc]) / s.CD) * (s.NSCZ - 0.001)) + 1
            msc_new = ((mscz - 1) * s.NSCX * s.NSCY +
                       (mscy - 1) * s.NSCX + (mscx - 1) +
                       s.NSCX * s.NSCY * s.NSCZ * mc)
            msc_new = max(0, min(MNSC - 1, msc_new))
            s.IPL[n] = msc_new
            s.PP[0, n] = x
            s.PP[1, n] = y
            s.PP[2, n] = z
            break    # done with this molecule, move to next n

        else:
            # while-True fell through without break → shouldn't happen
            pass

        if ift < 0 and n >= s.NM:
            # all existing molecules processed → let entering molecules in
            ift = 1
            enter3(s)
            # Don't increment n; the entering molecules are appended at end

        n += 1

