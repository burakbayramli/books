def write_restart(s: State):
    with open('DSMC3.RES', 'wb') as f:
        pickle.dump(s, f, protocol=pickle.HIGHEST_PROTOCOL)
    print("  Restart file written.")

def read_restart():
    with open('DSMC3.RES', 'rb') as f:
        s = pickle.load(f)
    print("  Restart file loaded.")
    return s

