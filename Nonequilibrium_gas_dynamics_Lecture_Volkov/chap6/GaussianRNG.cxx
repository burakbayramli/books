        double frand_Gaussian ( double E, double V ) ///////////////////////////////////////////////
        // Here E is the mean, V is the variance
        { //////////////////////////////////////////////////////////////////////////////////////////
	        return E + sqrt ( - 2.0 * V * log ( brng () ) ) * cos ( 2.0 * M_PI * brng () );
        } //////////////////////////////////////////////////////////////////////////////////////////
